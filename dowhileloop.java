// This program demonstrates the use of a do-while loop in Java. It initializes a counter variable and uses the loop to print a message a specified number of times, incrementing the counter with each iteration until a certain condition is met.
public class dowhileloop {
    public static void main(String[] args)
    {
        int i = 0;
        do{
            System.out.println("i love my parents");
            i++;
        }while(i<=10);
    }
}
